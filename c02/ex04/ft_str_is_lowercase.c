#include <unistd.h>

int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	count;

	i = 0;
	count = 0;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			count++;
		i++;
	}
	if (count == i || i == 0)
		return (1);
	else
		return (0);
}
